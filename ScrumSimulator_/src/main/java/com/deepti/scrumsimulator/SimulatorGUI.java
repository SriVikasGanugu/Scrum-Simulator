    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        if(btnedit.getText().equals("EDIT")){
            btnedit.setText("UPDATE");
            jTextField1.setText(jList1.getSelectedValue());
            jList1.setEnabled(false);
            btndelete.setEnabled(false);
            btnclear.setEnabled(false);
        }else{
            btnedit.setText("EDIT");
            int index= jList1.getSelectedIndex();
            listModel.setElementAt(jTextField1.getText(), index);
            btnedit.setEnabled(false);
            btndelete.setEnabled(false);
            btnadd.setEnabled(true);
            jTextField1.setText(null);
            jList1.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Product Updated");
            clearall();
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int index = jList1.getSelectedIndex();
        int reply = JOptionPane.showConfirmDialog(null, "Delete Product?","Delete", JOptionPane.YES_NO_OPTION);
        if(reply == JOptionPane.YES_OPTION){
            listModel.removeElementAt(index);
            JOptionPane.showMessageDialog(null, "Product Deleted");
        }else{
            btnedit.setEnabled(false);
        btndelete.setEnabled(false);
        btnadd.setEnabled(true);
        clearall();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnclearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnclearActionPerformed
        // TODO add your handling code here:
        int reply = JOptionPane.showConfirmDialog(null,"Delete Product?","Delete", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION){
            listModel.removeAllElements();
            JOptionPane.showMessageDialog(null, "All Product Deleted");
        }
        btnedit.setEnabled(false);
        btndelete.setEnabled(false);
        btnadd.setEnabled(true);
    }//GEN-LAST:event_btnclearActionPerformed

    
