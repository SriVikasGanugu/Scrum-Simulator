    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            DatabaseConnection connection = new DatabaseConnection("UserAuthentication");
            Connection c = connection.createConnection();
            Statement statement = c.createStatement();
            String DeleteBacklogQuery = "DELETE FROM Backlog WHERE BacklogId = '" + BacklogId + "';";
            statement.executeUpdate(DeleteBacklogQuery);
        } catch (SQLException ex) {
            Logger.getLogger(Register_Page.class.getName()).log(Level.SEVERE, null, ex);
        }

        for (int i = 0; i < listModel.getSize(); i++) {
            String userstoryId = generateUniqueUserstoryID();
            String storyDetails = (String) listModel.getElementAt(i);
            insertIntoBacklog(BacklogId, scrumId, userstoryId, storyDetails);
        }

        JOptionPane.showMessageDialog(null, "Data inserted into Backlog table.");
        ProductOwner_ProuctBacklog feedbackform = new ProductOwner_ProuctBacklog(scrumId, username);
        feedbackform.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    
    public void insertIntoBacklog(String backlogId, String scrumId, String userstoryId, String storyDetails) {
        try {
            DatabaseConnection connection = new DatabaseConnection("UserAuthentication");
            Connection c = connection.createConnection();
            Statement statement = c.createStatement();
            String insertQuery = "INSERT INTO Backlog (BacklogId, ScrumID, UserStoryID, StoryDetails) VALUES "
                    + "('" + backlogId + "', '" + scrumId + "', '" + userstoryId + "', '" + storyDetails + "')";
            statement.executeUpdate(insertQuery);
        } catch (SQLException ex) {
            Logger.getLogger(SimulatorGUI.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            // Close resources (Statement, Connection, etc.) in a real application
        }
    }
    
    public void clearall(){
        if(jList1.getModel().getSize()>0){
            btnclear.setEnabled(true);
        }else{
            btnclear.setEnabled(false);
        }
    }
